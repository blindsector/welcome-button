// =======================
// ПОДГОТОВКА
// =======================
const baseVerbs = verbsText
  .split(/\s+/)
  .map(v => v.trim().toLowerCase())
  .filter(v => v.length > 0);

// за уникалност
const usedForms = new Set();

// ако има повторение → добавяме число
function makeUnique(word) {
  if (!usedForms.has(word)) {
    usedForms.add(word);
    return word;
  }
  let i = 2;
  while (usedForms.has(word + i)) i++;
  const newWord = word + i;
  usedForms.add(newWord);
  return newWord;
}

// =======================
// ГЕНЕРАТОР НА ФОРМИ
// =======================
function generateVerbForms(verb) {
  const forms = [];

  // корен (много грубо)
  let stem = verb;

  // махаме типични окончания
  if (verb.endsWith("ам") || verb.endsWith("ям")) {
    stem = verb.slice(0, -2);
  } else if (verb.endsWith("я")) {
    stem = verb.slice(0, -1);
  } else if (verb.endsWith("а")) {
    stem = verb.slice(0, -1);
  }

  // === СЕГАШНО ВРЕМЕ ===
  forms.push(verb);                 // 1 л. ед. ч.
  forms.push(stem + "еш");          // 2 л. ед. ч.
  forms.push(stem + "е");           // 3 л. ед. ч.
  forms.push(stem + "ем");          // 1 л. мн. ч.
  forms.push(stem + "ете");         // 2 л. мн. ч.
  forms.push(stem + "ат");          // 3 л. мн. ч. (грубо)

  // === МИНАЛО СВЪРШЕНО ПРИЧАСТИЕ ===
  forms.push(stem + "л");           // м.р.
  forms.push(stem + "ла");          // ж.р.
  forms.push(stem + "ло");          // ср.р.
  forms.push(stem + "ли");          // мн.ч.

  // === БЪДЕЩЕ ВРЕМЕ ===
  forms.push("ще " + verb);
  forms.push("ще " + stem + "еш");
  forms.push("ще " + stem + "е");
  forms.push("ще " + stem + "ем");
  forms.push("ще " + stem + "ете");
  forms.push("ще " + stem + "ат");

  // === ПОВЕЛИТЕЛНО ===
  forms.push(stem + "й");           // ед.ч.
  forms.push(stem + "йте");         // мн.ч.

  // === ДЕЕПРИЧАСТИЕ ===
  forms.push(stem + "йки");

  return forms;
}

// =======================
// ГЕНЕРИРАМЕ ВСИЧКИ ФОРМИ
// =======================
const allVerbForms = baseVerbs.map(v => generateVerbForms(v));

// =======================
// СДВОЯВАНЕ
// =======================
const dictionaryPairs = [];

for (let i = 0; i < allVerbForms.length - 1; i += 2) {
  const formsA = allVerbForms[i];
  const formsB = allVerbForms[i + 1];

  const len = Math.min(formsA.length, formsB.length);

  for (let j = 0; j < len; j++) {
    const a = makeUnique(formsA[j]);
    const b = makeUnique(formsB[j]);
    dictionaryPairs.push(`${a} : ${b}`);
  }
}

